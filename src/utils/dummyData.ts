export function generateDummyRecords(count: number) {
  const records = [];
  const states = Object.keys(indianStatesDistricts);
  const genders = ["Male", "Female"];
  
  const startDate = new Date('2023-01-01');
  const endDate = new Date('2024-12-31');

  for (let i = 0; i < count; i++) {
    const state = states[Math.floor(Math.random() * states.length)];
    const stateData = indianStatesDistricts[state];
    const district = stateData.districts[Math.floor(Math.random() * stateData.districts.length)];
    
    const randomDate = new Date(startDate.getTime() + Math.random() * (endDate.getTime() - startDate.getTime()));
    const dateString = randomDate.toISOString().split('T')[0];

    const vaccineType = vaccineTypes[Math.floor(Math.random() * vaccineTypes.length)];
    const vaccineDoses = vaccineInfo[vaccineType].doses;
    const dose = vaccineDoses[Math.floor(Math.random() * vaccineDoses.length)];
    
    const name = indianNames[Math.floor(Math.random() * indianNames.length)];
    const emailName = name.toLowerCase().replace(/ /g, '.');

    records.push({
      certId: `VAX-${dateString.replace(/-/g, '')}-${String(i).padStart(6, '0')}`,
      name: name,
      email: `${emailName}@example.com`,
      age: Math.floor(Math.random() * 70) + 10, // Age between 10 and 80
      gender: genders[Math.floor(Math.random() * genders.length)],
      state: state,
      district: district.name,
      latitude: district.lat + (Math.random() - 0.5) * 0.1, // Add some variance
      longitude: district.lng + (Math.random() - 0.5) * 0.1,
      vaccineType: vaccineType,
      dose: dose,
      dateAdministered: dateString,
      administeringOfficer: `Dr. ${indianNames[Math.floor(Math.random() * indianNames.length)].split(' ')[0]}`
    });
  }

  return records;
}